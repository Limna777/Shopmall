<template>
  <swiper>
    <swiper-item v-for="item in banners">
      <a :href="item.link">
        <img :src="item.image" @load="imgLoad">
      </a>
    </swiper-item>
  </swiper>
</template>

<script>
import { Swiper, SwiperItem } from 'components/common/swiper'
export default {
  name: 'HomeSwiper',
  props:{
    banners:{
      type:Array,
      default(){
        return[]
      }
    }
  },
  data() {
    return {
      isLoad:false
    }
  },
  methods: {
  imgLoad(){
    if(!this.isLoad){
    this.$emit('imgLoad')
    this.isLoad = true
    }
  }
  },
  components: {
    Swiper,SwiperItem
  }
}
</script>

